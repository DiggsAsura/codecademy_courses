# Let's create some functions which can help us solve match problems! For this first function, we are going to
# take the tenth power of a number. In order to do this we need to do three things:

# 1. Set up the function header for tenth_power which accepts one parameter
# 2. Take the tenth power of the input value
# 3. Return the result

# Ok this is kinda hilarious, but also a bit releiving. Feels like the difficulty-curve just went back to easy
# mode. Knock on woods. 

def tenth_power(num):
	return num ** 10

# Easy peasy! Feels good haha. But also feels a bit like cheating. However no hint, no nothing. I probably 
# read up good enough during these chapters.


# Uncomment these function calls to test your tenth_power function:
print(tenth_power(1))
# 1 to the 10th power is 1
print(tenth_power(0))
# 0 to the 10th power is 0
print(tenth_power(2))
# 2 to the 10th power is 1024
