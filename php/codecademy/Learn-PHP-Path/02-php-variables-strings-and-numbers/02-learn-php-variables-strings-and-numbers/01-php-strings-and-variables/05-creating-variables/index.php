<?php
$my_name = "Kenny B";
$my_age = 39;

echo "My name is " . $my_name . " and I am " . $my_age . " years old.";
echo "\n";

// We are actually doing two things when creating a variable:
// 1. We are declaring a new variable by giving it a name $my_name
// 2. Then we are assigning it a value "Kenny B"
//
// $:           Sigil
// my_name:     Variable name
// =:           Assignment operator
// "Kenny B":   Variable value
// ;:           End of statement

// Variable names is case senstive and can not start with a number.

// Convention for variable names is snake_case
