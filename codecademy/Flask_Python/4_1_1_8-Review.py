# The front-end of a website or application consists of the HTML, CSS, 
# JavaScript and static assets sent to a client, like a web browser.

# A web server is a process running on a computer somewhere that listens for 
# incoming requests for information over the internet and sends back
# responses.

# Storing, acessing, and manipulating data is a large part of a web application's
# back-end.

# Data is stored in databases which can be relational databases or NoSQL 
# databases.

# The server-side of a web application, sometimes called the application 
# server, handles important tasks such as authorization and authentication.

# The back-end of web application often has a web API which is a way of interacting
# with an application's data through HTTP requests and responses.

# Together the technologies used to build front-end and back-end of a web
# application are known as the stack, and many different languages and 
# frameworks can be used to build a robust back-end. 

