/*
When you introduce a new piece of data into a JavaScript program, the browser saves it as an instance of the date
type. Every string instance has a property called length that stores the number of characters in that string. You
can retrieve property information by appending the string with a period and the property name:

console.log('Hello'.length); --> prints 5

The . is another operator! We call it the dot operator. 

In the example above, the value saved to the length property is retrieved from the instance of the string,
'Hello'. The program prints 5 to the console, because Hello has five characters in it.
*/

