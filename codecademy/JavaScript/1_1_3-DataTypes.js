/*
Theres seven fundamental data types in js
* Number: Any number, including numbers with decimals: 4, 8, 1516, 23.42... 
* String: Any grouping of characters on your keyboard (letters, numbers, spaces, symbols, etc) surrounded by single
  quotes: ' ... ' or double quotes " ... ". Though we prefer single quotes. Some people like to think of strings
  as fancy word for text
* Boolean: This data type only has two possible values - either true or false (without quotes). It's helpful
	to think of booleans as on and off switches or as the answers to a "yes" or "no" question.
* Null: This data type represents the intentional absence of a value, and is represented by the keyword 
	null (without quotes).
* Undefined: This data type is denoted by the keyword undefined (without quotes). It also represents the absence of 
	a value though it has a different use than null.
* Symbol: A newer feature to the language, symbols are unique identifiers, useful in more complex coding. No need
	to worry about these for now.
* Object: Collections of related data.
*/

